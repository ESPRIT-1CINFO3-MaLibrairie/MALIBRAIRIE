public class fournisseur {

    private String nom;
    private String prenom;
    private String email;

    private int id;
    private int cin;



    // Constructor
    public fournisseur(String nom, String prenom, String email, int id, int cin) {
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.id = id;
        this.cin = cin;
    }
}
